def load_mapping_definition() -> Dict[str, Any]:
    """マッピング定義ファイルを読み込む"""
    mapping_file = Path(__file__).parent / "mapping_definition.json"
    if mapping_file.exists():
        with open(mapping_file, "r", encoding="utf-8") as f:
            return json.load(f)
    return {}

